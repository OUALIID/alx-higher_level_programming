-- The script displays the number of records in the first_table of the database.
/*
- SELECT COUNT (*): Counts the number of rows in a table.
- AS count_89: Create a count_89 alias for the count result.
- FROM: Specifies the table from which you want to count rows.
- First_table: the name of the table whose rows you want to count.
*/
