-- The script displays the number of records in the first_table of the database.
/*
    - SELECT COUNT(*): Counts the number of rows in a table.
    - AS count_89: Creates an alias called count_89 for the count result.
    - FROM: Specifies the table from which you want to count rows.
    - first_table: The name of the table for which you want to count rows.
*/
SELECT COUNT(*) AS count_89 FROM first_table WHERE id = 89;
